public class Main {
    public static void main (String[] args) {

        System.out.println(Math.max(1, 99));
        System.out.println(Math.min(1, 99));
        System.out.println("Square root of 36: " + Math.sqrt(36));
        System.out.println("Absolute value of -3.14: " + Math.abs(-3.14)); 

        System.out.println("3.5 rounded: " + Math.round(3.5f));
        System.out.println("3.9 rounded down: " + Math.floor(3.9f));
        System.out.println("3.1 rounded up: " + Math.ceil(3.1f));
    }
}
