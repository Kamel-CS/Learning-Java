public class Numbers {
    public static void main(String[] args) {

        // integer types
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l = 9223372036854775807L;

//      ---

        // floating point types
        float f = 3.14f;
        double d = 19.99d;

        // Scientific notation, e indicates power of 10
        float f1 = 214e3f;
        double d1 = 23E3d;
    }
}
